                    <h2>¿Qué es el Pasado Simple?</h2>
                    <p>
                        El pasado simple se usa para hablar de acciones que ya terminaron en el pasado.
                        Es como contar una historia de algo que sucedió antes.
                    </p>
                    
                    <h3>¿Cómo se forma?</h3>
                    
                    <div class="example-box">
                        <h4>Verbos Regulares</h4>
                        <div class="english">Verbo + ed</div>
                        <div class="spanish">
                            work → work<strong>ed</strong> (trabajé)<br>
                            play → play<strong>ed</strong> (jugué)<br>
                            watch → watch<strong>ed</strong> (miré)
                        </div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Verbos Irregulares (¡Hay que memorizarlos!)</h4>
                        <div class="english">
                            go → <strong>went</strong> (fui)<br>
                            eat → <strong>ate</strong> (comí)<br>
                            see → <strong>saw</strong> (vi)<br>
                            have → <strong>had</strong> (tuve)
                        </div>
                    </div>
                    
                    <h3>Reglas para agregar -ed:</h3>
                    
                    <div class="example-box">
                        <h4>Reglas básicas:</h4>
                        <div class="english">
                            • Verbo normal: walk → walk<strong>ed</strong><br>
                            • Verbo terminado en -e: live → liv<strong>ed</strong><br>
                            • Una vocal + consonante: stop → stopp<strong>ed</strong><br>
                            • Terminado en -y: study → studi<strong>ed</strong>
                        </div>
                        <div class="spanish">
                            caminar → caminé<br>
                            vivir → viví<br>
                            parar → paré<br>
                            estudiar → estudié
                        </div>
                    </div>
                    
                    <h3>Ejemplos de Pasado Simple:</h3>
                    
                    <div class="example-box">
                        <h4>Afirmativo</h4>
                        <div class="english">I visited my grandmother yesterday.</div>
                        <div class="spanish">Visité a mi abuela ayer.</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Negativo</h4>
                        <div class="english">
                            I <strong>didn't</strong> go to school yesterday.<br>
                            She <strong>didn't</strong> watch the movie.
                        </div>
                        <div class="spanish">
                            No fui a la escuela ayer.<br>
                            Ella no vio la película.
                        </div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Preguntas</h4>
                        <div class="english">
                            <strong>Did</strong> you finish your homework?<br>
                            <strong>Did</strong> he eat breakfast?
                        </div>
                        <div class="spanish">
                            ¿Terminaste tu tarea?<br>
                            ¿Él desayunó?
                        </div>
                    </div>
                    
                    <h3>Palabras Clave del Pasado Simple:</h3>
