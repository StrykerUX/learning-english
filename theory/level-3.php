                <div class="theory-content">
                    <h2>¿Qué es el Presente Continuo?</h2>
                    <p>
                        El presente continuo se usa para hablar de acciones que están sucediendo ahora mismo,
                        en este momento. Es como una fotografía de lo que está pasando en este instante.
                    </p>
                    
                    <h3>¿Cómo se forma?</h3>
                    
                    <div class="example-box">
                        <h4>Estructura Básica</h4>
                        <div class="english">Sujeto + am/is/are + Verbo + ing</div>
                        <div class="spanish">I am eating = Yo estoy comiendo</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Conjugación del verbo "to be"</h4>
                        <div class="english">
                            • I <strong>am</strong> working<br>
                            • You <strong>are</strong> studying<br>
                            • He/She/It <strong>is</strong> sleeping<br>
                            • We <strong>are</strong> playing<br>
                            • They <strong>are</strong> running
                        </div>
                        <div class="spanish">
                            Yo estoy trabajando<br>
                            Tú estás estudiando<br>
                            Él/Ella está durmiendo<br>
                            Nosotros estamos jugando<br>
                            Ellos están corriendo
                        </div>
                    </div>
                    
                    <h3>¿Cómo agregar -ing al verbo?</h3>
                    
                    <div class="example-box">
                        <h4>Reglas básicas:</h4>
                        <div class="english">
                            • Verbo normal: work → work<strong>ing</strong><br>
                            • Verbo terminado en -e: make → mak<strong>ing</strong><br>
                            • Una vocal + consonante: run → runn<strong>ing</strong>
                        </div>
                        <div class="spanish">
                            trabajar → trabajando<br>
                            hacer → haciendo<br>
                            correr → corriendo
                        </div>
                    </div>
                    
                    <h3>Ejemplos de Presente Continuo:</h3>
                    
                    <div class="example-box">
                        <h4>Afirmativo</h4>
                        <div class="english">I am reading a book right now.</div>
                        <div class="spanish">Estoy leyendo un libro ahora mismo.</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Negativo</h4>
                        <div class="english">
                            She is<strong>n't</strong> watching TV.<br>
                            We are <strong>not</strong> playing soccer.
                        </div>
                        <div class="spanish">
                            Ella no está viendo televisión.<br>
                            No estamos jugando fútbol.
                        </div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Preguntas</h4>
                        <div class="english">
                            <strong>Are</strong> you studying English?<br>
                            <strong>Is</strong> he coming to the party?
                        </div>
                        <div class="spanish">
                            ¿Estás estudiando inglés?<br>
                            ¿Él viene a la fiesta?
                        </div>
                    </div>
                    
                    <h3>Palabras Clave del Presente Continuo:</h3>
                    <div class="example-box">
                        <div class="english">now, right now, at this moment, currently, today</div>
                        <div class="spanish">ahora, ahora mismo, en este momento, actualmente, hoy</div>
