                    <h2>¿Qué es el Futuro Simple?</h2>
                    <p>
                        El futuro simple se usa para hablar de cosas que van a pasar después de ahora.
                        Es como hacer planes o predicciones sobre el mañana.
                    </p>
                    
                    <h3>Dos formas de expresar el futuro:</h3>
                    
                    <div class="example-box">
                        <h4>1. Will + Verbo base</h4>
                        <div class="english">I + will + work = I will work</div>
                        <div class="spanish">Yo trabajaré</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>2. Going to + Verbo base</h4>
                        <div class="english">I + am going to + work = I am going to work</div>
                        <div class="spanish">Yo voy a trabajar</div>
                    </div>
                    
                    <h3>¿Cuándo usar cada forma?</h3>
                    
                    <div class="example-box">
                        <h4>Usa WILL para:</h4>
                        <div class="english">
                            • Promesas: "I will help you"<br>
                            • Decisiones espontáneas: "I will buy it"<br>
                            • Predicciones: "It will rain tomorrow"
                        </div>
                        <div class="spanish">
                            Te ayudaré<br>
                            Lo compraré<br>
                            Lloverá mañana
                        </div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Usa GOING TO para:</h4>
                        <div class="english">
                            • Planes: "I am going to study tonight"<br>
                            • Intenciones: "She is going to travel"<br>
                            • Algo que va a pasar pronto: "Look! It's going to rain"
                        </div>
                        <div class="spanish">
                            Voy a estudiar esta noche<br>
                            Ella va a viajar<br>
                            ¡Mira! Va a llover
                        </div>
                    </div>
                    
                    <h3>Ejemplos de Futuro Simple:</h3>
                    
                    <div class="example-box">
                        <h4>Afirmativo con Will</h4>
                        <div class="english">I will visit my friend tomorrow.</div>
                        <div class="spanish">Visitaré a mi amigo mañana.</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Negativo con Will</h4>
                        <div class="english">
                            I <strong>won't</strong> go to the party.<br>
                            She <strong>will not</strong> come today.
                        </div>
                        <div class="spanish">
                            No iré a la fiesta.<br>
                            Ella no vendrá hoy.
                        </div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Preguntas</h4>
                        <div class="english">
                            <strong>Will</strong> you come to my birthday?<br>
                            <strong>Are</strong> you <strong>going to</strong> study English?
                        </div>
                        <div class="spanish">
                            ¿Vendrás a mi cumpleaños?<br>
                            ¿Vas a estudiar inglés?
                        </div>
                    </div>
                    
                    <h3>Palabras Clave del Futuro:</h3>
                    <div class="example-box">
                        <div class="english">tomorrow, next week, next year, in the future, soon, later</div>
                        <div class="spanish">mañana, la próxima semana, el próximo año, en el futuro, pronto, más tarde</div>
