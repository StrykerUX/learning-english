                    <h2>¿Qué es el Presente Simple?</h2>
                    <p>
                        El presente simple es el tiempo verbal más básico en inglés. Se usa para hablar de:
                    </p>
                    <ul style="margin-left: 2rem; margin-bottom: 1rem;">
                        <li>Hechos generales y verdades universales</li>
                        <li>Rutinas y hábitos diarios</li>
                        <li>Estados permanentes</li>
                    </ul>
                    
                    <h3>¿Cómo se forma?</h3>
                    
                    <div class="example-box">
                        <h4>Estructura Básica</h4>
                        <div class="english">Sujeto + Verbo + Complemento</div>
                        <div class="spanish">I + work + in an office = Yo trabajo en una oficina</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Con He/She/It agregamos -s al verbo</h4>
                        <div class="english">
                            • I work → He work<strong>s</strong><br>
                            • I play → She play<strong>s</strong><br>
                            • I watch → It watche<strong>s</strong>
                        </div>
                        <div class="spanish">
                            Yo trabajo → Él trabaja<br>
                            Yo juego → Ella juega<br>
                            Yo miro → (Eso) mira
                        </div>
                    </div>
                    
                    <h3>Ejemplos de Presente Simple:</h3>
                    
                    <div class="example-box">
                        <h4>Con I/You/We/They</h4>
                        <div class="english">I eat breakfast every morning.</div>
                        <div class="spanish">Yo desayuno todas las mañanas.</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Con He/She/It</h4>
                        <div class="english">She work<strong>s</strong> at a hospital.</div>
                        <div class="spanish">Ella trabaja en un hospital.</div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Negativo</h4>
                        <div class="english">
                            I do<strong>n't</strong> like coffee.<br>
                            He does<strong>n't</strong> work on weekends.
                        </div>
                        <div class="spanish">
                            No me gusta el café.<br>
                            Él no trabaja los fines de semana.
                        </div>
                    </div>
                    
                    <div class="example-box">
                        <h4>Preguntas</h4>
                        <div class="english">
                            <strong>Do</strong> you like pizza?<br>
                            <strong>Does</strong> she speak English?
                        </div>
                        <div class="spanish">
                            ¿Te gusta la pizza?<br>
                            ¿Ella habla inglés?
                        </div>
                    </div>
                    
                    <h3>Palabras Clave del Presente Simple:</h3>
                    <div class="example-box">
                        <div class="english">usually, always, never, often, sometimes, every day, on Mondays</div>
                        <div class="spanish">usualmente, siempre, nunca, a menudo, a veces, todos los días, los lunes</div>
