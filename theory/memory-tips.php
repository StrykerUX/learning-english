                        <li><strong>Asociación Rápida:</strong> Desarrollas rapidez mental para conectar inglés y español</li>
                        <li><strong>Concentración:</strong> Mejoras tu atención sostenida y foco mental</li>
                        <li><strong>Confianza:</strong> Cada pareja encontrada aumenta tu confianza con el vocabulario</li>
                        <li><strong>Aprendizaje Activo:</strong> Participas activamente en place de solo memorizar pasivamente</li>
