        <div class="content-section">
            <h2 class="section-title">
                <i class="fas fa-brain"></i>
                ¿Cómo Funciona la Memoria?
            </h2>
            <div class="note">
                El juego del memorama aprovecha la memoria visual y de asociación. Cuando veas las cartas repetidamente, tu cerebro comenzará a crear conexiones automáticas entre las palabras en inglés y español. Esto es clave para el aprendizaje de idiomas.
            </div>
        </div>
        
        <div class="content-section">
            <h2 class="section-title">
                <i class="fas fa-lightbulb"></i>
                Técnicas Efectivas de Memorización
            </h2>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-image"></i>
                    1. Visualización
                </div>
                <div class="note">
                    Cuando veas una palabra, crea una imagen mental relacionada con ella. Esto ayuda a fijar la palabra en tu memoria visual.
                    <br><br>
                    <strong>Ejemplo:</strong><br>
                    <strong>Cat (Gato):</strong> Imagínate un gato específico - quizás tu mascota o un gato famoso de dibujos animados. Mientras más vívida sea la imagen, mejor recordarás la palabra.
                </div>
            </div>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-link"></i>
                    2. Asociación de Sonidos
                </div>
                <div class="note">
                    Busca similitudes en los sonidos entre el inglés y el español, o crea rimas que te ayuden a recordar.
                    <br><br>
                    <strong>Ejemplo:</strong><br>
                    <strong>Apple (Manzana):</strong> Puedes asociar "Apple" con "apel-ar" (suena similar). Imagínate a alguien apellándose a una manzana.
                </div>
            </div>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-map-marked-alt"></i>
                    3. Método de Posición
                </div>
                <div class="note">
                    Recuerda dónde estaban las cartas en el tablero. Esta técnica usa la memoria espacial.
                    <br><br>
                    <strong>Consejo:</strong><br>
                    Divide mentalmente el tablero en cuadrantes (arriba-izquierda, arriba-derecha, etc.) y asocia ciertos tipos de palabras con cada área.
                </div>
            </div>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-history"></i>
                    4. Repetición Espaciada
                </div>
                <div class="note">
                    Cuando encuentres una pareja, repite ambas palabras en voz alta varias veces. Esto refuerza la conexión en tu memoria.
                    <br><br>
                    <strong>Técnica:</strong><br>
                    Al encontrar "Dog - Perro", di: "Dog es Perro, Perro es Dog" tres veces seguidas.
                </div>
            </div>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-puzzle-piece"></i>
                    5. Agrupación por Categorías
                </div>
                <div class="note">
                    Mentalmente agrupa las palabras por temas (animales, objetos, etc.) para crear patrones de memoria más fuertes.
                    <br><br>
                    <strong>Grupos comunes:</strong>
                </div>
                <div class="memory-pair">
                    <span class="english-side">Animales</span>
                    <span>Cat, Dog</span>
                    <span class="spanish-side">Gato, Perro</span>
                </div>
                <div class="memory-pair">
                    <span class="english-side">Objetos</span>
                    <span>Car, Book</span>
                    <span class="spanish-side">Coche, Libro</span>
                </div>
            </div>
        </div>
        
        <div class="content-section">
            <h2 class="section-title">
                <i class="fas fa-chart-line"></i>
                Estrategias Durante el Juego
            </h2>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-eye"></i>
                    Observación Activa
                </div>
                <div class="note">
                    <ul>
                        <li>Mira cada carta por 2-3 segundos antes de voltearla</li>
                        <li>Presta atención a la posición de cada carta que veas</li>
                        <li>No te apures; la memoria necesita tiempo para procesar</li>
                    </ul>
                </div>
            </div>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-route"></i>
                    Rutas de Búsqueda
                </div>
                <div class="note">
                    <ul>
                        <li>Comienza por las esquinas y bordes</li>
                        <li>Sigue un patrón sistemático (izquierda a derecha, arriba a abajo)</li>
                        <li>No saltes de un lado a otro aleatoriamente</li>
                    </ul>
                </div>
            </div>
            
            <div class="rule-card">
                <div class="rule-title">
                    <i class="fas fa-comments"></i>
                    Narración Interna
                </div>
                <div class="note">
                    <ul>
                        <li>Mientras juegas, narra mentalmente: "Aquí hay un gato, necesito encontrar 'Cat'"</li>
                        <li>Relaciona las palabras con tu experiencia personal</li>
                        <li>Crea mini-historias con las palabras que encuentras</li>
                    </ul>
                </div>
            </div>
        </div>
        
        <div class="content-section">
